const MIN_WIDTH = 600;
const MIN_HEIGHT = 500;
const NOISE_AMPLITUDE= 100; // controls the offset in the Z axis of the tiles

let CODE_WIDTH, TILE_WIDTH;

let z_offsets;

function setup() {
  createCanvas(max(windowWidth, MIN_WIDTH), max(windowHeight, MIN_HEIGHT), WEBGL);
  randomSeed(42);
  setCodeDimensions();
  generateOffsets();
  ortho();
}

function draw() {
  // orbitControl(2,2,2);
  background(220);
  noStroke();
  // rotateY(PI / 16);

  textFont('Helvetica');
  text("Move your mouse in the window to discover the QR-code.", 10, 10)

  rotateY(map(mouseX, 0, windowWidth, -PI/3, PI/3));
  rotateX(map(mouseY, 0, windowHeight, PI/3, -PI/3));

  translate(-CODE_WIDTH/2, -CODE_WIDTH/2);
  for (let row=0; row<41; row++) {
    for (let col=0; col<41; col++) {
      push();
      translate(row * TILE_WIDTH, col * TILE_WIDTH, z_offsets[row][col]);
      selectFill(row, col);
      box(TILE_WIDTH);
      pop();
    }
  }

}

function selectFill(row, col) {
  if (min(row, col) >= PIXEL_ART_OFFSET && max(row, col) < PIXEL_ART_OFFSET + 16) {
    fill(PIXEL_ART[col - PIXEL_ART_OFFSET][row - PIXEL_ART_OFFSET]);
  } else {
    fill(QR_CODE[row][col] * 255);
  }
}

function windowResized() {
  resizeCanvas(windowWidth, windowHeight);
  setCodeDimensions();
  ortho();
}

function setCodeDimensions() {
  CODE_WIDTH = 0.75 * min(windowWidth, windowHeight);
  TILE_WIDTH = CODE_WIDTH / 41;
}

function generateOffsets() {
  z_offsets = Array(41);
  for (let row=0; row<41; row++) {
    z_offsets[row] = Array(41);
    for (let col=0; col<41; col++) {
      z_offsets[row][col] = NOISE_AMPLITUDE * randomGaussian();
    }
  }
}
